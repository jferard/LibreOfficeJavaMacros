package com.github.jferard.odfmavenplugin;

public class Util {
    public static String insertSuffix(final String name, final String suffix) {
        int i = name.lastIndexOf('.');
        if (i == -1) {
            return name + suffix;
        } else {
            return name.substring(0, i)+suffix+name.substring(i);
        }
    }
}
